package main;
import "fmt";

func main() {
	fmt.Println("\n***** LITERALS AND VALUES *****\n\n");
	var c int = 7 + 3 ;
	fmt.Println("1 + 1 =", c, "\n");

	var d int = 7 - 3 ;
	fmt.Println("7 - 3 = ", d, "\n");

	var e int = 7/3;
	fmt.Println("7/3 = ", e, "\n");

	var f int = 7*3;
	fmt.Println("7*3 = ", f, "\n");

}
